package VoterAssignment;

public class VoterDetails {
	private String voterFirstName;
	private String voterWardName;
	private int voterId;
	private long voterMobileNo;
	public String getVoterFirstName() {
		return voterFirstName;
	}
	public void setVoterFirstName(String voterFirstName) {
		this.voterFirstName = voterFirstName;
	}
	
	public String getVoterWardName() {
		return voterWardName;
	}
	public void setVoterWardName(String voterWardName) {
		this.voterWardName = voterWardName;
	}
	public int getVoterId() {
		return voterId;
	}
	public void setVoterId(int voterId) {
		this.voterId = voterId;
	}
	public long getVoterMobileNo() {
		return voterMobileNo;
	}
	public void setVoterMobileNo(long voterMobileNo) {
		this.voterMobileNo = voterMobileNo;
	}

}
